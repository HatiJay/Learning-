# Use the 'len()' function to tell us the number of items in the list
races = ["Running", "Swimming", "Rowing", "Cycling"]
print(len(races))

# Use the 'append()' function to add an item to the list
races.append("Long Distance")
print(races)

# Insert an element to a specific place in a list using the 'insert()' function
races.insert(1, "Climbing")
print(races)

# Remove items from a list using 'pop()' function
races.pop(2)
print(races)