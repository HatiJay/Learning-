# List of players in a team 
players = ["Kymille", "Hatice", "Douglas", "Jamiah", "Akina", "Shauna"]

# Slice first two players into variable 'g1'
print("Group 1: ")
print(players[:2])

# Slice third and forth players into variable 'g2'
print("Group 2: ")
print(players[2:4])

# Slice fith and sixth players into variable 'g3'
print("Group 3: ")
print(players[4:6])

# Slicing using negative indexting to print 'Akina' and 'Shauna'
print("Final two")
print(players[-2:])